     */
    public function show($slug)
    {

        $coba = DB::table('cobas')
            ->where('slug', $slug)
            ->get();




        if ($coba == '[]') {
            return response()->json(['status' => 404, 'message' => 'Data tidak ditemukan']);
        } else if ($coba != '[]') {
            return response()->json(['status' => 200, 'message' => 'Data berhasil ditampilkan', 'data' => $coba]);
        }
    }

